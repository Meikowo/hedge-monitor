  for select to anon, authenticated using (true);

-- ---------- 8. 前端视图（M3 前端的读取契约，现在定稿） ----------
drop view if exists public.v_ann_flow;
create view public.v_ann_flow as
select
  a.ann_id, a.code, a.name, a.title, a.publish_time, a.ann_date,
  a.pdf_url, a.source, a.matched_keywords, a.status, a.note,
  e.is_hedge_related, e.ann_role, e.scope, e.instruments, e.underlyings,
  e.venue, e.venues_detail, e.approval_level, e.plan_label, e.meeting,
  e.period_text, e.is_revolving, e.use_own_funds, e.summary,
  e.confidence, e.evidence, e.prompt_version, e.extracted_at,
  em.event_key,
  c.ind_l1, c.ind_l2, c.ind_l3, c.ent_type, c.province, c.market, c.scale
from public.announcements a
left join public.extractions e on e.ann_id = a.ann_id
left join public.event_members em on em.ann_id = a.ann_id
left join public.companies c on c.code = a.code;

drop view if exists public.v_events;
create view public.v_events as
select
  ev.*,
  c.ind_l1, c.ind_l2, c.ind_l3, c.ent_type, c.province, c.market, c.scale
from public.hedge_events ev
left join public.companies c on c.code = ev.code;
