#!/usr/bin/env python3
# -*- coding: utf-8 -*-
"""
prompt_extract.py —— 公告结构化抽取提示词（版本化）
====================================================
改动提示词必须递增 PROMPT_VERSION，抽取结果会记录该版本号，
以便后续区分不同提示词版本产出的数据质量。

设计要点（对应 PRD 5.7 防幻觉纪律的第一层）：
- 数值必须是原文出现的数字；找不到 → null，绝不推算/换算补全；
- 每个额度必须附原文摘录与页码（正文带【P页码】标记）；
- 口径（basis）是一等公民字段，宁可"未披露"不可猜；
- ann_role 区分公告角色，使制度/可行性/进展不混入实质计划统计。
"""

PROMPT_VERSION = "v2.0"

SYSTEM_PROMPT = """你是A股上市公司公告信息抽取引擎。你只输出一个JSON对象，不输出任何解释、前言或Markdown代码块围栏。
铁律：
1. 只抽取公告原文中明确写出的信息。原文没有的字段填 null（数组字段填 []），绝不猜测、推断、估算或换算补全。
2. 金额数字必须能在原文中找到对应表述；每条额度必须给出原文摘录(raw)和页码(page)。
3. 你区分"计划开展套保的实质公告"与"制度/可行性分析/进展/平仓"等其他角色，不混淆。
4. 判断不确定时，用"未披露"或 null，并降低 confidence。"""

INSTRUCTION = """从下面的公告中抽取结构化字段，输出严格符合以下结构的JSON对象（正文中【P数字】为页码标记）：

{
  "is_hedge_related": true|false,
  // 是否与"套期保值/衍生品风险管理业务"相关。制度、可行性分析、进展、平仓公告也算 true。
  // 以下情况为 false：股权激励期权、证券投资/理财、重组报告书或审计报告仅顺带提及套保、
  // 期货公司自身经营公告、与套保无关的其他公告。

  "ann_role": "计划-董事会"|"计划-股东大会"|"可行性分析"|"管理制度"|"进展"|"平仓或终止"|"风险提示"|"其他",
  // 公告角色。"计划-董事会"=董事会审议通过开展套保业务的公告（含"关于开展20XX年度套期保值业务的公告"）；
  // "计划-股东大会"=股东大会审议通过该事项；若一份公告同时披露董事会已通过且尚待提交股东大会，仍填"计划-董事会"。

  "scope": ["商品"|"外汇"|"利率"|"其他"],        // 涉及的套保类别，可多选
  "instruments": ["期货","期权","远期结售汇","外汇远期","外汇掉期","货币互换","利率互换","场外衍生品","其他"],
  "underlyings": ["铜","铝","螺纹钢","美元","欧元"...],   // 原文明确列举的品种/币种；未列举则 []
  "venue": "境内"|"境外"|"境内外"|"未披露",       // LME、CME、SGX、芝商所等属境外
  "venues_detail": ["上期所","LME"...],           // 原文点名的交易所/市场，未点名则 []

  "approval_level": "董事会"|"股东大会"|"董事会及股东大会"|"未披露",
  // 该事项最终需要的审批层级（如原文写"无需提交股东大会"则为"董事会"）。

  "plan_label": "2026年度" | "2026-2028年度" | null,   // 标题或正文中的计划年度标签，没有则 null
  "meeting": "第五届董事会第十二次会议" | null,          // 审议会议届次
  "period_text": "自董事会审议通过之日起12个月内" | null,  // 授权期限原文
  "period_months": 12 | null,                            // 期限月数；仅当原文可直接得出（"12个月"=12，"一年"=12）
  "is_revolving": true|false|null,   // 额度是否可循环/滚动使用
  "use_own_funds": true|false|null,  // 是否明确使用自有资金（不含募集资金）

  "quotas": [
    // 每条额度一项。同一公告可能有多条（如商品外汇分列、境内外分列、保证金与合约价值并列）。
    {
      "scope": "商品"|"外汇"|"利率"|"综合",
      "basis": "保证金占用"|"业务总额"|"名义本金"|"合约价值"|"其他"|"未披露",
      // 口径定义（关键字段，宁缺毋错）：
      //   保证金占用 = "保证金最高占用额/占用保证金不超过…"；
      //   业务总额   = 外汇类"业务总额/累计金额/交易金额不超过…"；
      //   名义本金   = 明确写"名义本金"；
      //   合约价值   = "合约价值/合约金额/持仓合约价值不超过…"；
      //   其他       = 如"不超过净资产的10%"等无法归入上述口径的表述（amount 填 null，raw 记原文）。
      "amount": 50000000,        // 换算成"元"的数字：5,000万元=50000000；1.2亿元=120000000；7,000万美元=70000000
      "currency": "CNY"|"USD"|"EUR"|"HKD"|"JPY"|"其他",   // 人民币=CNY，美元=USD
      "raw": "保证金最高占用额不超过人民币5,000万元",       // 原文摘录，≤50字，必填
      "page": 2                  // 该表述所在页码（取自【P】标记），必填
    }
  ],

  "evidence": [
    // 除额度外的其他关键字段证据（venue/approval_level/period 至少各一条，若原文有）
    {"field": "venue", "quote": "原文摘录≤40字", "page": 3}
  ],

  "summary": "一句话概括（≤60字），如：公司拟开展2026年度铜铝套保，保证金上限5000万元，境内期货市场",
  "confidence": 0.0~1.0   // 对本次抽取整体准确性的自评
}

若 is_hedge_related=false：ann_role 填"其他"，其余业务字段全部 null/[]，quotas=[]，summary 一句话说明为何无关。

单位换算示例（只在原文有明确数字时换算）：
"1,000万元"=10000000；"3亿元"=300000000；"7,000万美元"=70000000（currency=USD）；"5亿元人民币"=500000000。

公告标题: {title}
证券简称: {name}({code})
公告正文（带页码标记）:
\"\"\"
{body}
\"\"\""""


def build_messages(title: str, name: str, code: str, body: str) -> list[dict]:
    user = (INSTRUCTION
            .replace("{title}", title or "(无标题)")
            .replace("{name}", name or "?")
            .replace("{code}", code or "?")
            .replace("{body}", body))
    return [
        {"role": "system", "content": SYSTEM_PROMPT},
        {"role": "user", "content": user},
    ]
